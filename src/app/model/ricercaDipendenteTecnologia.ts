export class RicercaDipendenteTecnologia {

    nome : string;
    cognome : string;
    nomeTecnologia : string;
    competenza : string;
    esperienza : string;

    constructor(nome : string, cognome : string, nomeTecnologia : string, competenza : string, esperienza : string){

        this.nome = nome;
        this.cognome = cognome;
        this.nomeTecnologia = nomeTecnologia;
        this.competenza = competenza;
        this.esperienza = esperienza;

    }

}